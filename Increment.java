//This is a program demonstrate the increment and decrement operators
class Increment {
	public static void main(String a[]){
		int i;
		i = 5;
		//This is a pre increment
		++i;
		System.out.println("Pre increment of 5 is " + i);
		i++;
		System.out.println("Post increment of 6 is " + i);
		               
	}
}