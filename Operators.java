//This is a program demonstrat the operators
class Operators {
	public static void main(String a[]){
		int no1,no2;
		int ans;
		no1 = 10;
		no2 = 5;
		ans = no1 + no2;
		System.out.println("Sum is " + ans);
		ans = no1 - no2;
		System.out.println("Sub is " + ans);
		ans = no1 * no2;
		System.out.println("Mul is " + ans);
		ans = no1 / no2;
		System.out.println("Div is " + ans);
	}
}